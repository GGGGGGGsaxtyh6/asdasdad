    r10 = 0x9e3779b97f4a7c15
    
    for i, byte in enumerate(input_str.encode()):
        # Cálculo de r9
        r9 = (i + 0x31) * byte
        
        # Cálculo de rcx (mod 7)
        rcx = i
        rax = i
        rax = rax * r14
        rax = rax - (rax >> 1)
        rdx = rax >> 2
        rax = rdx * 8
        rax = rax - rdx
        rcx = rcx - rax
        rcx = rcx + 1
        
        # Cálculo de rax
        rax = 0xd6e8feb86659fd93
        rax = rax >> (rcx & 0x3f)
        
        # XOR operations
        rcx = byte ^ i
        rax = rax ^ r9
        
        # Rotate left - usando rolq
        rax = ((rax << (rcx & 0x3f)) | (rax >> (64 - (rcx & 0x3f)))) & 0xffffffffffffffff
        
        # XOR with r8
        rax = rax ^ r8
        
        # Update r8
        r8 = (rax + r10) & 0xffffffffffffffff
        r8 = ((r8 << 11) | (r8 >> 53)) & 0xffffffffffffffff
        
        hash_val = rax
    
    return hash_val

